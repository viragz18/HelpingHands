    public void popOrganizationComboBoxT3() {
        comboOrg.removeAllItems();

        for (Network network : business.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise.getEnterpriseType().getValue().contains("NGO") || enterprise.getEnterpriseType().getValue().contains("Hospital")) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization.getName().contains("Finance") || organization.getName().contains("HospitalAdminAssistant")) {
                            comboOrg.addItem(organization);
                        }

                    }
                }
            }
        }


    }




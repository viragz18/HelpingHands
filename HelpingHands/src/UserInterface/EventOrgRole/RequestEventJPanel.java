    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        Date today = new Date();
        if (txtEventName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter event name", "Error", JOptionPane.ERROR_MESSAGE);

        } else if (dateChooserEventDate.getDate().before(today)) {

            JOptionPane.showMessageDialog(this, "Event date should be a future date", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (txtLocation.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter location", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (String.valueOf(spinParticipationNo.getValue()).equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter the expected participation", "Error", JOptionPane.ERROR_MESSAGE);
        } else if ((Integer) spinParticipationNo.getValue() <= 0) {
            JOptionPane.showMessageDialog(this, "Please enter the valid expected participation", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (txtBudget.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter budget", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (comboOrg.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Please selected the organization to whom request is to assigned", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String eventName = txtEventName.getText();
            String location = txtLocation.getText();
            double budget = Double.parseDouble(txtBudget.getText());
            int noOfPpl = (Integer) spinParticipationNo.getValue();

            Date eventDate = dateChooserEventDate.getDate();

            Event e = new Event();
            e.setEventName(eventName);
            e.setLocation(location);
            e.setEventDate(eventDate);
            e.setNoOfPpl(noOfPpl);
            e.setBudget(budget);

            Date date = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("h:mm:ss a");
            String formattedDate = sdf.format(date);

            EventCreateWorkRequest request = new EventCreateWorkRequest();
            request.setRequestDate(formattedDate);
            request.setRequestedby(userAccount);
            request.setEventName(eventName);
            request.setEventDate(eventDate);
            request.setRequestedOn(date);
            
            request.setEventLocation(location);
            request.setNoOfPpl(noOfPpl);

            request.setSender(userAccount);
            request.setOrganizationName(comboOrg.getSelectedItem().toString());
            if(comboOrg.getSelectedItem().toString().contains("HospitalAdminAssistant")){
                request.setDescription(eventName + " event at " + location + " and the required number of doctors is" + txtDoctors.getText());
                request.setNoOfDoctors(Integer.parseInt(txtDoctors.getText()));
                request.setBudget(0);

            }
            else{
                request.setDescription(eventName + " event at " + location + " and the required budget is" + budget);
                request.setNoOfDoctors(0);
                request.setDebit(budget);
                request.setNarration("Event Budget");
                request.setChequeNo("Budget released in Cash ");
                
            }

            request.setStatus("Sent");
            request.setReqByorganizationName(organization.getName());
            //enterprise.getWorkQueue().getWorkRequestList().add(request);

            Organization org = null;
            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (organization instanceof EventManagementOrganization) {
                    org = organization;
                    break;
                }
            }
            if (org != null) {
                //org.getWorkQueue().getWorkRequestList().add(request);
                //userAccount.getWorkQueue().getWorkRequestList().add(request);
                organization.getEventList().add(e);

                enterprise.getWorkQueue().getWorkRequestList().add(request);

                JOptionPane.showMessageDialog(null, "Request Sent Successfully");
            }

        }

    }//GEN-LAST:event_btnSubmitActionPerformed

